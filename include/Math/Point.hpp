#ifndef _CC_MATH_POINT_HPP
#define _CC_MATH_POINT_HPP

namespace cc
{
    //////////////////////////////////////////////////////////////////////////
    // Basic Definitions
    //////////////////////////////////////////////////////////////////////////

    template <typename T>
    struct Point2T
    {
        T x = T(0);
        T y = T(0);
    };

    template <typename T>
    struct Point3T
    {
        T x = T(0);
        T y = T(0);
        T z = T(0);
    };

    //////////////////////////////////////////////////////////////////////////
    // Useful type aliases
    //////////////////////////////////////////////////////////////////////////

    using Point2f = Point2T<float>;
    using Point3f = Point3T<float>;

    using Point2d = Point2T<double>;
    using Point3d = Point3T<double>;

    //////////////////////////////////////////////////////////////////////////
    // Operators
    //////////////////////////////////////////////////////////////////////////

    
}

#endif //_CC_MATH_POINT_HPP
